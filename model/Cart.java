package model;

/**
 *
 * @author kuros
 */
public class Cart {

    private int id;
    private int cusId;
    private double totalMoney;

    public Cart() {
    }

    public Cart(int id, int cusId, double totalMoney) {
        this.id = id;
        this.cusId = cusId;
        this.totalMoney = totalMoney;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getCusId() {
        return cusId;
    }

    public void setCusId(int cusId) {
        this.cusId = cusId;
    }

    public double getTotalMoney() {
        return totalMoney;
    }

    public void setTotalMoney(double totalMoney) {
        this.totalMoney = totalMoney;
    }

}
